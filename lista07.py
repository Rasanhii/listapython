nomes = ['Maria', 'José', 'João', 'Ana', 'Pedro']

#organizar lista de maneira ascendente
nomes.sort()

print(nomes)

minha_lista = [8, 10, 6, 2, 4]
minha_lista.sort()
print(minha_lista)


#organizar lista de maneira decrescende
nomes.sort(reverse = True)
print(nomes)

minha_lista.sort(reverse = True)
print(minha_lista)